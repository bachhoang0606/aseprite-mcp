_MISSING = ["11111", "10001", "10001", "10001", "10001", "10001", "11111"]


def measure(s, scale=1, tracking=1):
    n = len(s)
    if n == 0:
        return (0, GH * scale)
    w = (n * GW + (n - 1) * tracking) * scale
    return (w, GH * scale)


def text_image(s, scale=1, fg=(255, 255, 255, 255), tracking=1):
    """Render `s` (uppercased) to a transparent RGBA image, NN-upscaled by `scale`."""
    s = s.upper()
    w1 = len(s) * GW + max(0, len(s) - 1) * tracking
    img = Image.new("RGBA", (max(1, w1), GH), (0, 0, 0, 0))
    px = img.load()
    cx = 0
    for ch in s:
        g = _G.get(ch, _MISSING)
        for ry, row in enumerate(g):
            for rx, bit in enumerate(row):
                if bit == "1":
                    px[cx + rx, ry] = fg
        cx += GW + tracking
    if scale != 1:
        img = img.resize((img.width * scale, img.height * scale), Image.NEAREST)
    return img


def draw_text(dst, s, scale, cx, top, fg=(232, 224, 208, 255), tracking=1):
    """Paste centered-at-x text onto dst; returns (x, y, w, h) of the placed box."""
    t = text_image(s, scale=scale, fg=fg, tracking=tracking)
    x = int(cx - t.width / 2)
    dst.alpha_composite(t, (x, int(top)))
    return (x, int(top), t.width, t.height)
